#include<iostream>
using namespace std;

int main()
{
    int t;
    cin>>t;
    while(t--)
    {
        int a,b;
        cin>>a>>b;

        int d= a*3;
        int e=b*2;

        if(d>e)
        {
            cout<<e<<'\n';
        }
        else if(d<e)
        {
            cout<<d<<'\n';
        }
        else
        {
            cout<<d<<'\n';
        }
    
    }
}